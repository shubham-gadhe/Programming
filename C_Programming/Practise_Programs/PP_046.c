/*
==============================================================================
 Program     : Display "Jay Ganesh..." Five Times

 Description : Displays the message "Jay Ganesh..." five times using a
               for loop.

 Author      : Shubham Gadhe
==============================================================================
*/

#include<stdio.h>

int main()
{
    int iCnt = 0;

    // Display the message five times.
    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        printf("Jay Ganesh...\n");
    }
    
    return 0;
}

