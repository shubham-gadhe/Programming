/*
==============================================================================
 Program     : Display Numbers from 1 to 5 Using while Loop

 Description : Displays numbers from 1 to 5 using a while loop.

 Author      : Shubham Gadhe
==============================================================================
*/

#include<stdio.h>

int main()
{
    int iCnt = 0;

    iCnt = 1;

    while(iCnt <= 5)
    {
        printf("%d\t", iCnt);
        iCnt++;
    }

    printf("\n");
    
    return 0;
}

/*
==============================================================================
 Sample Output
------------------------------------------------------------------------------
 1    2    3    4    5
==============================================================================
*/