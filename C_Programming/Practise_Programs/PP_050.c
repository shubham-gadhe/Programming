/*
==============================================================================
 Program     : Display Numbers from 5 to 1 Using while Loop

 Description : Displays numbers from 5 down to 1 using a while loop.

 Author      : Shubham Gadhe
==============================================================================
*/

#include<stdio.h>

int main()
{
    int iCnt = 0;

    iCnt = 5;

    while(iCnt >= 1)
    {
        printf("%d\t", iCnt);
        iCnt--;
    }

    printf("\n");
    
    return 0;
}

/*
==============================================================================
 Sample Output
------------------------------------------------------------------------------
 5    4    3    2    1
==============================================================================
*/