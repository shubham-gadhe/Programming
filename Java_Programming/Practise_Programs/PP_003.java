//  Description  : This program displays the numbers
//                 from 1 to 10 using a for loop.
//
//  Example:
//      Output : 1
//               2
//               ..
//               10
//
//////////////////////////////////////////////////////////////////

class PP_003
{
    public static void main(String A[])
    {
        int iCnt = 0;

        for(iCnt = 1; iCnt <= 10; iCnt++)
        {
            System.out.println(iCnt);
        }    
    }
}